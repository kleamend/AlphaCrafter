export type AppCopy = (typeof dictionaries)[Locale];

export function getCopy(locale: Locale): AppCopy {
  return dictionaries[locale];
}

export function getAgentCopy(locale: Locale, agent: AgentPhase) {
  return dictionaries[locale].agents[agent];
}

export function getWorkspaceTabs(locale: Locale): Array<{ id: WorkspaceTab; label: string }> {
  const copy = getCopy(locale).workspace;
  return [
    { id: "activity", label: copy.activity },
    { id: "terminal", label: copy.terminal },
    { id: "output", label: copy.output },
    { id: "artifacts", label: copy.artifacts },
    { id: "metrics", label: copy.metrics },
  ];
}
